
public class VariablesPractice {

	public static void main(String[] args) {
		
		// create 2 variables
		String myName = "Stephanie";
		int myBirthYear = 1997;
		
		// output to the screen
		// both produce the same output
		String output = "My name is " + myName + " and I was born in " + myBirthYear;
		System.out.println(output);
	
		System.out.print("My name is " + myName + " and I was born in " + myBirthYear);

	}

}
